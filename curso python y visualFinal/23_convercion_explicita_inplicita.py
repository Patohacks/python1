# convercion INPLICITA

numero = 10 # numero entero (INT)
numero2 = 20.5 # numero decimal (float)
print(numero + numero2) # resultado tipo (float)

# para saber el tipo de dato.

print(type(numero)) 
print(type(numero2)) 

# convertir el tipo de dato float a int (decimal a entero)

print (numero + int(numero2))

# y comprobar su tipo de clase

print (type (numero + int(numero2)))