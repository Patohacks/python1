# metodo capitalice convierte la primera leta en mayusculas 
nombre = input("introdusca su nombre:\n")
nombre_convertido = nombre.capitalize()
print (f"hola,{nombre_convertido}")

# metodo upper convierte todo en mayusculas 
nombre = input("introdusca su nombre:\n")
nombre_convertido = nombre.upper()
print (f"hola,{nombre_convertido}")
       
# metodo lower convierte todo a minusculas 

nombre = input("introdusca su nombre:\n")
nombre_convertido = nombre.lower()
print (f"hola,{nombre_convertido}")